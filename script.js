/* Collapse */
document.getElementById('faqId').querySelectorAll('[faqItem]').forEach(obj => {
  let trocarRotacaoSeta = false

  obj.addEventListener('click', (event) => {
    if(event.target.classList[0] == 'faq__header') {
      obj.querySelector('.faq__content').style.display = trocarRotacaoSeta ? '' : 'flex' 
      obj.querySelector('[arrowRed]').style.transform = `rotate(${trocarRotacaoSeta ? 0 : 180}deg)` 
      trocarRotacaoSeta = !trocarRotacaoSeta
    }
  })
})

document.querySelectorAll('collapse').forEach(obj => {
  const alturaDoHeader = obj.getAttribute('alturaDoHeader')
  const valorDeReferenciaKey = obj.getAttribute('valorDeReferencia')

  data[valorDeReferenciaKey].forEach(element => {
    
    let abertoFechado = false
    const card = document.createElement('collapse-card')
    card.setAttribute('role', 'button')
    card.innerHTML = `
      <collapse-header style="height: ${alturaDoHeader}px">
        ${element.title}
        <collapse-arrow></collapse-arrow>
      </collapse-header>
      <collapse-content>${element.htmlContent}</collapse-content>
    `

    card.addEventListener('click', (e) => {
      abertoFechado ? e.currentTarget.classList.remove('collapse--open') : e.currentTarget.classList.add('collapse--open')
      abertoFechado = !abertoFechado
    })

    obj.appendChild(card)
  })
})